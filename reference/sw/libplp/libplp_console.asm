# libplp console driver 
# assumes libplp_vga is included and initialized


# globals
libplp_console_pos:
	.word 0	# x
	.word 0	# y
libplp_console_current:
	.word 0
libplp_console_buffer0:
	.space 76800 # 640x480 / 4 words
libplp_console_buffer1:
	.space 76800

# init
libplp_console_init:
	move $t7, $ra
	li $t0, libplp_console_current
	li $a0, libplp_console_buffer0
	sw $a0, 0($t0)
	jal libplp_vga_framebuffer
	nop
	li $a0, 0x00000000
	jal libplp_vga_clear
	nop
	li $t0, libplp_console_pos
	sw $zero, 0($t0)
	sw $zero, 4($t0)
	jal libplp_vga_enable
	nop
	jr $t7
	nop

# set position - a0 is x, a1 is y
libplp_console_set_pos:
	li $t0, libplp_console_pos
	sw $a0, 0($t0)
	sw $a1, 4($t0)
	jr $ra
	nop

# get position - v0 is x, v1 is y
libplp_console_get_pos:
	li $t0, libplp_console_pos
	lw $v0, 0($t0)
	lw $v1, 4($t0)
	jr $ra
	nop

# increment position
libplp_console_inc_pos:
	# get pos doesn't use t9, so we'll copy the return address there
	move $t9, $ra
	jal libplp_console_get_pos
	nop
	addiu $t0, $zero, 80 # columns
	addiu $t1, $zero, 40 # rows
	addiu $v0, $v0, 1
	beq $v0, $t0, libplp_console_inc_pos_of
	nop
	j libplp_console_inc_pos_done
	nop
	libplp_console_inc_pos_of:
		move $v0, $zero
		addiu $v1, $v1, 1
		beq $v1, $t1, libplp_console_inc_pos_of2
		nop
		j libplp_console_inc_pos_done
		nop
	libplp_console_inc_pos_of2:
		# copy row 1->479 into 0->478 of the other buffer
		# clear the last row of the other buffer
		# and swap buffers, setting the position to 0,479
		li $t0, libplp_console_current
		li $t1, libplp_console_buffer0
		li $t2, libplp_console_buffer1
		lw $t0, 0($t0)
		beq $t0, $t1, libplp_console_inc_pos_swap
		nop
		move $t3, $t1
		j libplp_console_inc_pos_next
		nop
		libplp_console_inc_pos_swap:
			move $t1, $t2
			move $t3, $t2
		libplp_console_inc_pos_next:
		# t0 has the current buffer, t1 the next
		li $t2, 307200
		addu $t2, $t2, $t0 # the end of the buffer
		addiu $t0, $t0, 7680
		libplp_console_inc_pos_loop:
			lw $t4, 0($t0)
			sw $t4, 0($t1)
			addiu $t0, $t0, 4
			addiu $t1, $t1, 4
			bne $t0, $t2, libplp_console_inc_pos_loop
			nop
		# finish off by clearing the last row on $t1
		move $t0, $zero
		ori $t2, $zero, 7680
		libplp_console_inc_pos_loop2:
			sw $zero, 0($t1)
			addiu $t0, $t0, 4
			addiu $t1, $t1, 4
			bne $t0, $t2, libplp_console_inc_pos_loop2
			nop
		li $t0, libplp_console_current
		sw $t3, 0($t0)
		move $a0, $t3
		jal libplp_vga_framebuffer
		nop
		ori $v0, $zero, 0
		ori $v1, $zero, 39
	libplp_console_inc_pos_done:
		move $a0, $v0
		move $a1, $v1
		jal libplp_console_set_pos
		nop
		jr $t9
		nop

# write routine
# this is the main routine the user will call
# character (ascii encoded) is in a0 (only lower 7 bits are used)
libplp_console_write:
	# none of our callers use t8, we'll safely store our ra there
	move $t8, $ra

	# calculate the offset in the font
	# zero off top 24 bits of a0
	addiu $t0, $zero, 127
	and $t0, $t0, $a0 # character is in t0
	addiu $t0, $t0, -32 # font doesn't render the first 32 characters
	addiu $t1, $zero, 96
	mullo $a1, $t0, $t1 # offset in the font
	li $t2, libplp_console_font_8x12
	addu $a1, $t2, $a1
	
	# calculate the offset in the vga framebuffer
	jal libplp_console_get_pos
	nop
	li $t0, 0xf0400000 # vga module
	lw $t0, 4($t0) # framebuffer base address
	# vga position = (row * 7680) + (col * 8) + base address
	ori $t1, $zero, 7680
	mullo $t1, $v1, $t1
	sll $t2, $v0, 3
	addu $a0, $t1, $t2
	addu $a0, $a0, $t0

	# write the character
	jal libplp_console_write_char
	nop

	# and increment the position
	jal libplp_console_inc_pos
	nop

	jr $t8
	nop	

# character copy
libplp_console_write_char:
	# there are 24 words in a character, two per row, and 12 rows
	# pixels are packed in the vga memory, so this is just a specialized memcpy
	move $t0, $zero # (0-7 rows)
	ori $t3, $zero, 12
	# $a0 has the vga buffer position
	# $a1 has the font buffer position
	write_char_row_copy:
		lw $t1, 0($a1)
		lw $t2, 4($a1)
		sw $t1, 0($a0)
		sw $t2, 4($a0)
		addiu $t0, $t0, 1
		addiu $a0, $a0, 640
		addiu $a1, $a1, 8
		bne $t3, $t0, write_char_row_copy
		nop
	jr $ra
	nop

